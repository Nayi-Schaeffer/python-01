#Ejecución condicional.
#Todos los programas hacen evaluaciones condicionales segun el valor de sus variables y expresiones. Por lo tanto no todas las lineas de codigo se ejecutan cuando corre un programa. 
#Para esto esta el concepto de expresión Booleana

#Ej:
print(5 < 4) #False
print(5 > 4) #True
print(4 != 4) #False
print(4 == 4) #True

#Cuando queremos comparar menor o igual el opreador es <= 
print(4 <= 6)

#El mayo o igual es >=
print(5 >= 6)

#No existe el =< ni el =>


#Podemos tener expresiones booleanas mas complejas utilizando los operadores and, or y not.

print("_____________Expresiones booleanas compuestas______________")
print(4 < 5 or 6 > 8)#True or False = Todo esto se simplifica en True 
